var number = parseInt(prompt("What is number?"), 10);

var nextNumber = number + 1;

console.log("The next number after " + number + " is " + nextNumber);

// The First Line Collects the number from a prompt and converts it to an integer of base 10.
// The Second Line Adds 1 to the number so you can get the next number after it.
// The Third Line Displays the result to the console.