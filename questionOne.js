var minutes = parseInt(prompt("How many Minutes"), 10);

var secs = minutes * 60;

console.log(minutes + " Minutes is equal to " + secs + " Secs");

// The First Line Collects the Minutes from a prompt and converts it to an integer of base 10.
// The Second Line Multiples the minutes by 60 in other to get the amount of secs it is equivalent to.
// The Third Line Displays the result to the console.